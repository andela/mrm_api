class DeleteResource(graphene.Mutation):
    """
        Delete a room resource
    """

    class Arguments:
        resource_id = graphene.Int(required=True)
        state = graphene.String()
    resource = graphene.Field(Resource)

    @Auth.user_roles('Admin', 'Super Admin')
    def mutate(self, info, resource_id):
        query_resources = Resource.get_query(info)
        exact_resource = query_resources.filter(and_(
            ResourceModel.state == "active",
            ResourceModel.id == resource_id)).first()
        if not exact_resource:
            return_error.report_errors_bugsnag_and_graphQL("Resource not found")
        update_entity_fields(exact_resource, state="archived")
        query_room_resource = RoomResource.get_query(info)
        room_resources = query_room_resource.filter(
            RoomResourceModel.resource_id == resource_id).all()
        for room_resource in room_resources:
            room_resource.delete()
        exact_resource.save()
        return DeleteResource(resource=exact_resource)


class Query(graphene.ObjectType):
    """
        Query to get room resources
    """

    all_resources = graphene.Field(
        PaginatedResource,
        page=graphene.Int(),
        per_page=graphene.Int(),
        unique=graphene.Boolean(),
        resource_labels=graphene.String(),
        description="Returns a list of paginated room resources and accepts the arguments\
            \n- page: Field for page of the room response\
            \n- per_page: Field for number of responses per page\
            \n- unique: Boolean field for uniqueroom response\
            \n- resource_labels: Labels to filter the resources with")
    get_resources_by_room_id = graphene.Field(
        lambda: RoomResources,
        room_id=graphene.Int(),
        description="Returns a list of room's resources. Accepts the argument\
            \n- room_id: Unique identifier of a room")

    resource_by_name = graphene.List(
        Resource,
        search_name=graphene.String(),
        description="Returns resources that match search name. Accepts the argument\
            \n- search_name: Unique name of a resource")

    rooms_containing_resource = graphene.List(
        RoomResource,
        resource_id=graphene.Int(),
        description="Returns a list of rooms containing the supplied resource id.\
            \n- resource_id: Unique identifier of a resource")

    def resolve_all_resources(self, info, **kwargs):
        # Get all resources
        response = PaginatedResource(**kwargs)
        return response

    @Auth.user_roles('Admin', 'Super Admin')
    def resolve_get_resources_by_room_id(self, info, room_id):
        # Get resources of a specific room
        exact_room = RoomSQLAlchemyObject.get_query(info).filter(
            RoomModel.id == room_id,
            RoomModel.state == "active"
        ).first()
        if not exact_room:
            return_error.report_errors_bugsnag_and_graphQL("Room not found")
        resource_query = Resource.get_query(info)
        room_resources = RoomResource.get_query(info).filter(
            RoomResourceModel.room_id == room_id
        )
        if not room_resources.first():
            return_error.report_errors_bugsnag_and_graphQL(
                "Room has no resource yet")
        resources = []
        for resource in room_resources:
            room_resource = resource_query.filter(
                ResourceModel.state == "active",
                ResourceModel.id == resource.resource_id
            ).first()
            room_resource.quantity = resource.quantity
            resources.append(room_resource)
        return RoomResources(roomResources=resources)

    @Auth.user_roles('Admin', 'Super Admin')
    def resolve_resource_by_name(self, info, search_name):
        matching_resources = []
        resource_name = search_name.lower().replace(" ", "")
        if not resource_name:
            return_error.report_errors_bugsnag_and_graphQL(
                "Please input Resource Name")
        all_resources = Resource.get_query(info).filter_by(state="active")
        for each_resource in all_resources:
            striped_resource = each_resource.name.lower().replace(" ", "")
            if resource_name in striped_resource:
                matching_resources.append(each_resource)
        if not matching_resources:
            return_error.report_errors_bugsnag_and_graphQL(
                'No Matching Resource')
        return matching_resources

    @Auth.user_roles('Admin', 'Super Admin')
    def resolve_rooms_containing_resource(self, info, resource_id):
        resource_exists = Resource.get_query(info).filter_by(
            id=resource_id).first()
        if not resource_exists:
            return_error.report_errors_bugsnag_and_graphQL(
                'Resource does not exist')
        query = RoomResource.get_query(info).join(ResourceModel).join(
            RoomModel)
        rooms_with_resource = query.filter(
            RoomResourceModel.resource_id == resource_id,
            ResourceModel.state == "active").filter(
            RoomModel.state == "active")
        return rooms_with_resource


class Mutation(graphene.ObjectType):
    """
        Mutation to create, update and delete resources
    """

    create_resource = CreateResource.Field(
        description="Creates a new Resource having arguments\
            \n- name: The name field of the resource[required]\
            \n- room_id: The unique identifier of the room where the resource \
            is created[required]\n- quantity: The number of resources[required]"
    )
    update_room_resource = UpdateRoomResource.Field(
        description="Updates the room resources fields below\
            \n- name: The name field of the resource\
            \n- room_id: The unique identifier of the room where the resource \
            is found\n- quantity: The number of resources")
    delete_resource = DeleteResource.Field(
        description="Deletes a resource with arguments\
            \n- resource_id: Unique id identifier of the resource\
            \n- state: Check if the room response is active,\
                 archived or deleted")
    assign_resource = AssignResource.Field(
        description="Mutation to assign a resource to a room with arguments\
            \n- roomId: Unique key identifier of a room[required]\
            \n- resourceId: Unique key identifier of a resource[required]\
            \n- quantity: The number of resources to be assigned to a room`"
    )
    update_assigned_resource = UpdateAssignedResource.Field(
        description="Updates the quantity of an assigned resource\
            \n- room_id: The room id of the room with the resource\
            \n- resource_id: The resource id\
            \n- quantity: The new quantity of the resource")
    delete_assigned_resource = DeleteAssignedResource.Field(
        description="Deletes an assigned resource in a room \
            \n- room_id: The room id of the room with the resource\
            \n- resource_id: The resource id"
    )
