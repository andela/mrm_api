class Resource(SQLAlchemyObjectType):
    """
        Autogenerated return type of a room resource
    """

    class Meta:
        model = ResourceModel


class RoomResource(SQLAlchemyObjectType):
    class Meta:
        model = RoomResourceModel


class RoomResources(graphene.ObjectType):
    roomResources = graphene.List(Resource)


class AssignResource(graphene.Mutation):
    """
        Assigns a resource to a room
    """
    class Arguments:
        resource_id = graphene.Int(required=True)
        quantity = graphene.Int(required=True)
        room_id = graphene.Int(required=True)
    room_resource = graphene.Field(RoomResource)

    @Auth.user_roles('Admin', 'Super Admin')
    def mutate(self, info, **kwargs):
        validate_empty_fields(**kwargs)
        query = RoomSQLAlchemyObject.get_query(info)
        active_rooms = query.filter(RoomModel.state == "active")
        exact_room = active_rooms.filter(
            RoomModel.id == kwargs['room_id']).first()
        if not exact_room:
            return_error.report_errors_bugsnag_and_graphQL("Room not found")
        exact_resource = ResourceModel.query.filter_by(
            id=kwargs['resource_id'], state="active").first()
        if not exact_resource:
            return_error.report_errors_bugsnag_and_graphQL(
                'Resource with such id does not exist.')
        resource_name = exact_resource.name
        assigned_quantity = kwargs['quantity']
        if assigned_quantity < 1:
            return_error.report_errors_bugsnag_and_graphQL(
                "Assigned quantity cannot be less than 1."
            )
        resource_query = RoomResource.get_query(info)
        exact_resource_id = resource_query.filter(
            RoomResourceModel.resource_id == kwargs['resource_id'],
            RoomResourceModel.room_id == kwargs['room_id']).first()
        if exact_resource_id:
            return_error.report_errors_bugsnag_and_graphQL(
                'Resource already exists in the room.'
            )
        room_resources = RoomResourceModel(**kwargs, name=resource_name)
        room_resources.save()
        return AssignResource(room_resource=room_resources)


class UpdateAssignedResource(graphene.Mutation):
    """
        Update assigned resource in a room
    """
    class Arguments:
        resource_id = graphene.Int(required=True)
        room_id = graphene.Int(required=True)
        quantity = graphene.Int(required=True)
    room_resource = graphene.Field(RoomResource)

    @Auth.user_roles('Admin', 'Super Admin')
    def mutate(self, info, **kwargs):
        resource_id = kwargs['resource_id']
        room_id = kwargs['room_id']
        room_resource_query = RoomResource.get_query(info)
        actual_resource = room_resource_query.filter_by(
            room_id=room_id, resource_id=resource_id).first()

        if not actual_resource:
            return_error.report_errors_bugsnag_and_graphQL(
                'Resource does not exist in the room')
        if kwargs['quantity'] < 0:
            return_error.report_errors_bugsnag_and_graphQL(
                'Assigned quantity cannot be less than zero'
            )
        update_entity_fields(actual_resource, **kwargs)
        actual_resource.save()
        return UpdateAssignedResource(room_resource=actual_resource)


class DeleteAssignedResource(graphene.Mutation):
    """
        Delete assigned resource in a room
    """
    class Arguments:
        resource_id = graphene.Int(required=True)
        room_id = graphene.Int(required=True)
    room_resource = graphene.Field(RoomResource)

    @Auth.user_roles('Admin', 'Super Admin')
    def mutate(self, info, resource_id, room_id):
        query = RoomResource.get_query(info)
        exact_room = RoomModel.query.filter_by(id=room_id).first()
        if not exact_room:
            return_error.report_errors_bugsnag_and_graphQL(
                'Room does not exist')
        exact_resource = ResourceModel.query.filter_by(
            id=resource_id).first()
        if not exact_resource:
            return_error.report_errors_bugsnag_and_graphQL(
                'Resource does not exist')
        assigned_resource = query.filter(
            RoomResourceModel.room_id == room_id,
            RoomResourceModel.resource_id == resource_id).first()
        if not assigned_resource:
            return_error.report_errors_bugsnag_and_graphQL(
                'The resource has not been assigned to the specified room')
        assigned_resource.delete()
        return DeleteAssignedResource(room_resource=assigned_resource)


class PaginatedResource(Paginate):
    """
        Returns paginated room resources
    """
    resources = graphene.List(Resource)

    def resolve_resources(self, info):
        # Function to get all room resources
        page = self.page
        per_page = self.per_page
        unique = self.unique
        resource_labels = self.filter_data.get('resource_labels')
        active_resources = Resource.get_query(info).filter(
            ResourceModel.state == "active")
        if resource_labels:
            active_resources_query = room_resources_join_room(active_resources)
            for resourse_label in resource_labels.split(','):
                active_resources = active_resources_query.filter(
                    cast(RoomModel.room_labels, String)
                    .ilike(f'%{resourse_label.strip()}%'))

        if not page:
            if unique:
                return active_resources.distinct(ResourceModel.name).all()
            return active_resources.order_by(
                func.lower(ResourceModel.name)).all()
        page = validate_page(page)
        self.query_total = active_resources.count()
        result = active_resources.order_by(func.lower(
            ResourceModel.name)).limit(per_page).offset(page*per_page)
        if result.count() == 0:
            return_error.report_errors_bugsnag_and_graphQL("No more resources")
        return result


class CreateResource(graphene.Mutation):
    """
        Create a room resource
    """

    class Arguments:
        name = graphene.String(required=True)
    resource = graphene.Field(Resource)

    @Auth.user_roles('Admin', 'Super Admin')
    def mutate(self, info, **kwargs):
        resource = ResourceModel(**kwargs)
        payload = {
            'model': ResourceModel, 'field': 'name', 'value':  kwargs['name']
        }
        with SaveContextManager(
            resource, 'Resource', payload
        ):
            return CreateResource(resource=resource)


